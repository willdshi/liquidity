function TF =isholiday(YYYYMMDD)
    TF = false;

    yyyy = floor(YYYYMMDD/10000);
    mm = floor((YYYYMMDD - yyyy*10000)/100);
    dd = YYYYMMDD - yyyy*10000 - mm*100;
    datestring = [num2str(yyyy) '-' num2str(mm) '-' num2str(dd)];

    % Ten standard U.S. national holidays, plus:
    %   -- 20130329 is Good Friday banking holiday
    %   -- 20121030 is Hurricane Sandy
    HOL = [ ...
        20010115,20020121,20030120,20040119,20050117,20060116,20070115,20080121,20090119,20100118,20110117,20120116,20130121,20140120,20150119,20160118, ...
        20010101,20020101,20030101,20031231,20050101,20060102,20070101,20080101,20090101,20091231,20110101,20120102,20130101,20140101,20150101,20160101, ...
        20010219,20020218,20030217,20030216,20050221,20060220,20070219,20080218,20090216,20090215,20110221,20120220,20130218,20140217,20150216,20160215, ...
        20010528,20020527,20030526,20040531,20050530,20060529,20070528,20080526,20090525,20100531,20110530,20120528,20130527,20140526,20150525,20160530, ...
        20010704,20020704,20030704,20030705,20050704,20060704,20070704,20080704,20090703,20090705,20110704,20120704,20130704,20140704,20150703,20160704, ...
        20010903,20020902,20030901,20030906,20050905,20060904,20070903,20080901,20090907,20090906,20110905,20120903,20130902,20140901,20150907,20160905, ...
        20011008,20021014,20031013,20031011,20051010,20061009,20071008,20081013,20091012,20091011,20111010,20121008,20131014,20141013,20151013,20161012, ...
                 20021111,20031111,20031111,20051111,                  20081111,20091111,20091111,20111111,         20131111,20141111,20151111,20161111, ...
        20011122,20021128,20031127,20031125,20051124,20061123,20071122,20081127,20091126,20091125,20111124,20121122,20131128,20141127,20151126,20161124, ...
        20011225,20021225,20031225,20041224,20051226,20061225,20071225,20081225,20091225,20101224,20111226,20121225,20131225,20141225,20151225,20161226, ...
        20130329, ...
        20121030 ...
    ];

    if ( weekday(datestring)==1 || weekday(datestring)==7 )
        TF = true;
    elseif (~isempty(HOL(HOL==YYYYMMDD)))
        TF = true;
    end       
    
end